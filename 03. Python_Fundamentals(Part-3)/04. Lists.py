# Lists-
# mutable sequence of values


# A list is a collection of items.
# It can store different types of data together.
# Example: [10, "Rajia", 5.6, True]


# Example 
marks = [90, 67, 89, 56, 77, 99, 82]
marks[5] = 90
print(marks)
print(len(marks))

for i in range(0, len(marks)):
    print(marks[i])

